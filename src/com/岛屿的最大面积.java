    public static int maxAreaOfIsland(int[][] grid) {
        int area = 0;
        int temp ;
        for (int i = 0; i < grid.length; i++) {
            for (int j = 0; j < grid[0].length; j++) {
                if (grid[i][j] == 1) {
                    temp = DFS(grid,0, i, j);
                    if (temp > area) {
                        area = temp;
                    }
                }
            }
        }
        return area;
    }

    private static int DFS(int[][] grid, int area, int i, int j) {
        if ( i < 0 || i >= grid.length||j < 0 ||j >= grid[0].length || grid[i][j] == 0){
            return 0;
        }
        grid[i][j] = 0;
        area += DFS(grid, 0, i + 1, j);
        area += DFS(grid, 0, i, j + 1);
        area += DFS(grid, 0, i - 1, j);
        area += DFS(grid, 0, i, j - 1);
        return area + 1;
    }
